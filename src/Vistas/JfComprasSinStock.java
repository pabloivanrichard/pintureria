     private void calcularTotal() {
    float total=0;    
    for(int i=0; i<jTable1.getRowCount();i++){
    float precio= Float.parseFloat(jTable1.getValueAt(i, 2).toString());
    float cantidad=Float.parseFloat(jTable1.getValueAt(i, 3).toString());
    float calculo= precio*cantidad;
    total=total+calculo;
    }
    jTextField4.setText(String.valueOf(total));
    }
    private void inicializarComboProveedor() {
    DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
     List<Proveedor> lista;
     lista=new FacadeProveedor().listar();
     if(lista.get(0).getId()!=null){
      for(int i=0;  i < lista.size(); i++){
      Vector vector = new Vector();
      vector.add(lista.get(i).getNombre());
      modeloCombo.addElement(lista.get(i).getNombre());
     }
     jComboBox1.setModel(modeloCombo);            
    }      
    }
