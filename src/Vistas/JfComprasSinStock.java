    private void CargarDatoscompra() {
    //modelo2=new DefaultTableModel();
    jComboBox1.setSelectedItem(compra.getProveedor().getNombre());
    jXDatePicker1.setDate(compra.getFecha());
    for(int i=0; i< compra.getDetallecompra().size();i++){
    Vector vector=new Vector();
    vector.add( compra.getDetallecompra().get(i).getProducto());
    vector.add(compra.getProveedor().getNombre());
    vector.add( compra.getDetallecompra().get(i).getPrecio());
    vector.add( compra.getDetallecompra().get(i).getCantidad());
    modelo2.addRow(vector);
    }
    jTable1.setModel(modelo2);
    jTextField4.setText(String.valueOf(compra.getPrecio_totol()));
    }
    
