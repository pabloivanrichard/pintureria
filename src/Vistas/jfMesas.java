    private void cargarTabla() {
    EntityManagerFactory emf= Persistence.createEntityManagerFactory("pintureriaPU");
    List <Mesa> listap=new FacadeMesa().listar();
    DefaultTableModel modelo= new DefaultTableModel();
    modelo.addColumn("Id");   
    modelo.addColumn("Numero Mesa");
    modelo.addColumn("Descripcion Mesa");
    modelo.addColumn("Estado");
    for(int i=0; i<listap.size(); i++){
    Vector vector=new Vector();
    vector.add(listap.get(i).getId());
    vector.add(listap.get(i).getNumero());
    vector.add(listap.get(i).getDescripcion());
    int estado=listap.get(i).getCerrado();
    String vestado=null;
    if(estado==0){
    vestado="ABIERTO";
    }
    if(estado==1){
    vestado="CERRADO";
    }
    vector.add(vestado);
     modelo.addRow(vector);
    }
    jTable1.setModel(modelo);
   
    }
