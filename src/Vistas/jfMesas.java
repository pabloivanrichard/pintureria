    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().equals("")&&!jTextField2.getText().equals("")){
            EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
            Mesa m=new Mesa();
            m.setNumero(Integer.parseInt(jTextField2.getText()));
            m.setDescripcion(jTextField1.getText().toUpperCase());
            m.setCerrado(1);
            MesaJpaController mjc=new MesaJpaController(emf);
            mjc.create(m);
            JOptionPane.showMessageDialog(rootPane, "La operacion se realizo con exito");
            limpiarCampos();
            cargarTabla();
        }else{JOptionPane.showMessageDialog(rootPane, "Debe Completar todos los Datos");}
    }//GEN-LAST:event_jButton1ActionPerformed

