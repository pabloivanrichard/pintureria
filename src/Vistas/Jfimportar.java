 private void SeleccionarBD() {
        int resultado;

        BuscarImportar ventana = new BuscarImportar(null, true);

        FileNameExtensionFilter filtro = new FileNameExtensionFilter("XLS", "xls");
       // FileNameExtensionFilter filtro = new FileNameExtensionFilter("CSV", "csv");
 
        ventana.chooser.setFileFilter(filtro);

        resultado = ventana.chooser.showOpenDialog(null);

        if (JFileChooser.APPROVE_OPTION == resultado) {
            
           
                fichero = ventana.chooser.getSelectedFile();
                jTextField1.setText(fichero.getName());

        }
    }

