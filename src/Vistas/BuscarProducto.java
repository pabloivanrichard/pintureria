    private void llenarTablaNombre() {
    EntityManagerFactory emf= Persistence.createEntityManagerFactory("pintureriaPU");
    //List <Producto> listap=new FacadeProducto().buscarxnombre(jTextField1.getText().toUpperCase());
    //List <Almacen>  listaa=new FacadeAlmacen().buscarxnombre(jTextField1.getText().toUpperCase());
    List <Almacen> listap=new FacadeAlmacen().buscarxnombre(jTextField1.getText().toUpperCase());
    DefaultTableModel modelo= new DefaultTableModel();
    modelo.addColumn("Id");
    modelo.addColumn("Codigo");
    modelo.addColumn("Producto");
    modelo.addColumn("Talle");
    modelo.addColumn("Proveedor");
    modelo.addColumn("Cantidad");
    modelo.addColumn("precio real");
    modelo.addColumn("precio contado");
    for(int i=0; i<listap.size(); i++){
    Vector vector=new Vector();
    vector.add(listap.get(i).getId());
    vector.add(listap.get(i).getProducto().getCod());
    vector.add(listap.get(i).getProducto().getDescripcion());
    vector.add(listap.get(i).getProducto().getTalle());
    vector.add(listap.get(i).getProducto().getProveedor().getNombre());
    vector.add(listap.get(i).getCantidad());
    vector.add(listap.get(i).getProducto().getPrecio().getPrecio_real());
    vector.add(listap.get(i).getProducto().getPrecio().getPrecio_contado());    
    modelo.addRow(vector);
    }
    jTable1.setModel(modelo);   
    }

