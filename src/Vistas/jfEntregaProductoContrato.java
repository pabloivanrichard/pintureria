    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(contrato!=null && !jTextField3.getText().equals("")){
            try {
                EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
                Entrega_Producto_Contrato entrega=new Entrega_Producto_Contrato();
                entrega.setFecha(jXDatePicker1.getDate());
                entrega.setRetirador(jTextField3.getText().toUpperCase());
                entrega.setUsu(JLogin.usu);
                Entrega_Producto_ContratoJpaController epjc=new Entrega_Producto_ContratoJpaController(emf);
                epjc.create(entrega);
                //Lo Agrego al contrato y cambio el estado
                contrato.setEntrega(entrega);
                contrato.setEstado("CERRADO");
                ContratoJpaController cjc=new ContratoJpaController(emf);
                cjc.edit(contrato);
                JOptionPane.showMessageDialog(rootPane, "La Operacion se Realizo con Exito");
            } catch (Exception ex) {
                Logger.getLogger(jfEntregaProductoContrato.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

