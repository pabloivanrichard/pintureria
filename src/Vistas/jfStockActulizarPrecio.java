    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         if(jTable1.getSelectedRow()!=-1){
      EntityManagerFactory emf = Persistence.createEntityManagerFactory("pintureriaPU");  
      String valorTabla=(jTable1.getValueAt(jTable1.getSelectedRow(),0)).toString();
      Long valor=Long.parseLong(valorTabla);
      productoAlmacen=new AlmacenJpaController(emf).findAlmacen(valor);
      jTextField4.setText(productoAlmacen.getProducto().getCod());
      jTextField6.setText(productoAlmacen.getProducto().getDescripcion()+" "+productoAlmacen.getProducto().getTalle());
      jTextField5.setText(productoAlmacen.getProducto().getProveedor().getNombre());
      jTextField11.setText(String.valueOf(productoAlmacen.getProducto().getPrecio().getPrecio_contado()));
      jTextField7.setText(String.valueOf(productoAlmacen.getCantidad()));
      jTextField8.setText(String.valueOf(productoAlmacen.getAlerta()));
      jTextField9.setText(String.valueOf(productoAlmacen.getLocalizacion()));
      jTextField10.setText(String.valueOf(productoAlmacen.getProducto().getPrecio().getPrecio_real()));
        }
    }//GEN-LAST:event_jTable1MouseClicked

