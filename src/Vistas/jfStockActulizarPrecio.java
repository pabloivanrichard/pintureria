    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!jTextField12.getText().equals("")){
            try {
                EntityManagerFactory emf= Persistence.createEntityManagerFactory("pintureriaPU");
                Producto producto=productoAlmacen.getProducto();
                Precio precio=productoAlmacen.getProducto().getPrecio();
                precio.setPrecio_contado(Float.parseFloat(jTextField12.getText()));
                producto.setPrecio(precio);
                    ProductoJpaController pjc=new ProductoJpaController(emf);
                    PrecioJpaController prjc=new PrecioJpaController(emf);
                    prjc.edit(precio);
                    pjc.edit(producto);
                    llenarTablaProd();
                    JOptionPane.showMessageDialog(rootPane, "Se Actualizo el precio del producto");
                jTextField11.setText(String.valueOf(productoAlmacen.getProducto().getPrecio().getPrecio_contado()));
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(jfStockActulizarPrecio.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(jfStockActulizarPrecio.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } else {JOptionPane.showMessageDialog(rootPane, "Debe ingresar el nuevo precio del producto");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

