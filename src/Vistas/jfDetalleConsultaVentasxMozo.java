    private void CargarDatos() {
    jTextField1.setText(servicio.getMesa());
    jTextField2.setText(servicio.getMozo());
    SimpleDateFormat formato=new SimpleDateFormat("dd-MM-yyyy");
    SimpleDateFormat formato1=new SimpleDateFormat("HH:mm:ss");
      String MyFecha = formato.format(servicio.getFecha());
      String inicio = formato1.format(servicio.getInicio());
      String fin = formato1.format(servicio.getFin());
      jTextField3.setText(inicio);
      jTextField4.setText(fin);
      jXDatePicker1.setDate(servicio.getFecha());
     //Llenar la tabla detalle
       DefaultTableModel modelo2=new DefaultTableModel();
       modelo2.addColumn("Id");
       modelo2.addColumn("Producto");
       //modelo2.addColumn("Talle");
       modelo2.addColumn("Proveedor");
       modelo2.addColumn("Precio");
       modelo2.addColumn("Cantidad");
       modelo2.addColumn("Total");
       modelo2.addColumn("Es compuesto");
       
       
       for(int i=0; i<servicio.getDetalle().size();i++){
         Vector vector=new Vector();
         vector.add(servicio.getDetalle().get(i).getIdprodcuto());         
         vector.add(servicio.getDetalle().get(i).getProducto());
         vector.add("NEGOCIO");
         vector.add(servicio.getDetalle().get(i).getMonto());
         vector.add(servicio.getDetalle().get(i).getCantidad());
         vector.add(servicio.getDetalle().get(i).getMonto() * servicio.getDetalle().get(i).getCantidad() );
         vector.add(servicio.getDetalle().get(i).getCompuesto());
         modelo2.addRow(vector);
       }
       jTable1.setModel(modelo2);
       calcularTotal();
      
    }
    private void calcularTotal() {
    if(jTable1.getRowCount()>0){
    float total=0;    
    for(int i=0; i<jTable1.getRowCount();i++){
    float precio= Float.parseFloat(jTable1.getValueAt(i, 3).toString());
    float cantidad=Float.parseFloat(jTable1.getValueAt(i, 4).toString());
    float calculo= precio*cantidad;
    total=total+calculo;
    }
    jTextField5.setText(String.valueOf(total));    
    }else{jTextField5.setText(String.valueOf(0));} 
    
    }

