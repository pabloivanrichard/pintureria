    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
      if (jTable1.getSelectedRow() != -1) {
      EntityManagerFactory emf = Persistence.createEntityManagerFactory("pintureriaPU");  
      String valorTabla=(jTable1.getValueAt(jTable1.getSelectedRow(),0)).toString();
      Long valor=Long.parseLong(valorTabla);
      alumno= new AlumnoJpaController(emf).findAlumno(valor);
      JOptionPane.showMessageDialog(rootPane, "Selecciono:"+ alumno.getNombre() +" "+alumno.getApellido());
      jTextField7.setText(alumno.getOrg().getNombre());
      jTextField1.setText(alumno.getApellido());
      jTextField2.setText(alumno.getNombre());
      jTextField3.setText(alumno.getDni());
      jComboBox1.setSelectedItem(alumno.getTipodoc());
      jTextField8.setText(alumno.getPromocion());
      if(alumno.getTel()!=null){jTextField4.setText(alumno.getTel());}
      if(alumno.getEmail()!=null)
      {    
      jTextField5.setText(alumno.getEmail());
      }
      if(alumno.getDomicilio()!=null){
      jTextField6.setText(alumno.getDomicilio());}
      }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().equals("")&&!jTextField2.getText().equals("")&&!jTextField3.getText().equals("")&&!jTextField7.getText().equals("")&&!jTextField8.getText().equals("")){
        EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
        Alumno alu=new Alumno();
        alu.setApellido(jTextField1.getText().toUpperCase());
        alu.setNombre(jTextField2.getText().toUpperCase());
        alu.setTipodoc(jComboBox1.getSelectedItem().toString());
        alu.setDni(jTextField3.getText());
        alu.setTel(jTextField4.getText());
        alu.setEmail(jTextField5.getText());
        alu.setDomicilio(jTextField6.getText().toUpperCase());
        alu.setOrg(organizacion);
        alu.setPromocion(jTextField8.getText());
        AlumnoJpaController ajc=new AlumnoJpaController(emf);
        ajc.create(alu);
        buscarAlumno();
        limpiarCampos();
        }else{JOptionPane.showMessageDialog(rootPane, "Los campos con * son Obligatorios");}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().equals("")&&!jTextField2.getText().equals("")&&!jTextField3.getText().equals("")&&!jTextField7.getText().equals("")&&!jTextField8.getText().equals("")){
            try {
                EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
                Alumno alu=alumno;
                alu.setApellido(jTextField1.getText().toUpperCase());
                alu.setNombre(jTextField2.getText().toUpperCase());
                alu.setTipodoc(jComboBox1.getSelectedItem().toString());
                alu.setDni(jTextField3.getText());
                alu.setTel(jTextField4.getText());
                alu.setEmail(jTextField5.getText());
                alu.setDomicilio(jTextField6.getText().toUpperCase());
                alu.setOrg(organizacion);
                alu.setPromocion(jTextField8.getText());
                AlumnoJpaController ajc=new AlumnoJpaController(emf);
                ajc.edit(alu);
                buscarAlumno();
                limpiarCampos();
            } catch (Exception ex) {
                Logger.getLogger(jfAlumnos.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }else{JOptionPane.showMessageDialog(rootPane, "Los campos con * son Obligatorios");}
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
                if(alumno!=null){
                    try {
                        EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
                        AlumnoJpaController ajc=new AlumnoJpaController(emf);
                        ajc.destroy(alumno.getId());
                        buscarAlumno();
                        limpiarCampos();
                    } catch (NonexistentEntityException ex) {
                        Logger.getLogger(jfAlumnos.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un alumno para eliminar");}
                
                
    }//GEN-LAST:event_jButton4ActionPerformed

