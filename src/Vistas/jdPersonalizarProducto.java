    private void llenarTablaProducto() {
    List<Detalle_Contrato> lista=new ArrayList<>();
    lista=contrato.getDetalle_contrato();
    DefaultTableModel modeloTabla = new DefaultTableModel(); 
    Vector<String> columnas = new Vector<String>();
    modeloTabla.addColumn("Id");
    modeloTabla.addColumn("Producto");
    modeloTabla.addColumn("Monto");
    modeloTabla.addColumn("Cantidad");
       
    
    
    
    for (int i = 0; i < lista.size(); i++) {
      Vector vector = new Vector();
      vector.add(lista.get(i).getId());
      vector.add(lista.get(i).getProducto().getDescripcion());
      vector.add(lista.get(i).getMonto());
      vector.add(lista.get(i).getCantidad());
      
      
      modeloTabla.addRow(vector);
    }
     jTable1.setModel(modeloTabla); 
    
    
    }

