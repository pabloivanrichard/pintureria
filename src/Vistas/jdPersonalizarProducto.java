    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!jTextField5.getText().equals("")&& !jTextField6.getText().equals("")){
            int total=Integer.parseInt(jTextField3.getText());
            int personalizado=Integer.parseInt(jTextField4.getText());
            
            if(total >personalizado){
            try {
                EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
                PersonalizarContrato pc=new PersonalizarContrato();
                List<Producto> lp=new FacadeProducto().buscarxnombre(jTextField2.getText());
                pc.setProducto(lp.get(0)); 
                pc.setAlumno(alumno);
                pc.setTalle(jTextField6.getText().toUpperCase());
                if(!jTextArea1.getText().equals("")){
                    pc.setObservacion(jTextArea1.getText());
                }
                listapersonalizado.add(pc);
                ContratoJpaController cjc=new ContratoJpaController(emf);
                contrato.setComprapersonalizada(listapersonalizado);
                cjc.edit(contrato);
                llenarTablaPersonalizados();
                limpiarCampos();
            } catch (Exception ex) {
                Logger.getLogger(jdPersonalizarProducto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{JOptionPane.showMessageDialog(rootPane, "No se puede realizar la operacion, todos los Producto ya fueron Personalizados");}
        limpiarCampos();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        // TODO add your handling code here:
        if(jTable3.getSelectedRow()!=-1){
            try {
                EntityManagerFactory emf = Persistence.createEntityManagerFactory("pintureriaPU");
                String valorTabla=(jTable3.getValueAt(jTable3.getSelectedRow(),0)).toString();
                Long valor=Long.parseLong(valorTabla);
                PersonalizarContratoJpaController pjc=new PersonalizarContratoJpaController(emf);
                pc=pjc.findPersonalizarContrato(valor);
                jTextField5.setText(pc.getAlumno().getApellido()+" "+pc.getAlumno().getNombre());
                jTextField6.setText(pc.getTalle());
                if(!pc.getObservacion().equals("")){
                    jTextArea1.setText(pc.getObservacion());
                }
                pjc.edit(pc);
            } catch (Exception ex) {
                Logger.getLogger(jdPersonalizarProducto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jTable3MouseClicked

