    private void llenarTablaPagos() {
    SimpleDateFormat formato=new SimpleDateFormat("dd-MM-yyyy");
    List<detalle_cuenta_contrato> lista=new ArrayList<>();
    lista=contrato.getCuenta_contrato().getListadetallecuenta();
    DefaultTableModel modeloTabla = new DefaultTableModel(); 
    Vector<String> columnas = new Vector<String>();
    modeloTabla.addColumn("Id");
    modeloTabla.addColumn("Integrante");
    modeloTabla.addColumn("Fecha Entrega");
    modeloTabla.addColumn("Monto Entrega");
    float contadorpag=0;   
    
    
    
    for (int i = 0; i < lista.size(); i++) {
      if(lista.get(i).getAnulado().equals("NO")){  
      Vector vector = new Vector();
      vector.add(lista.get(i).getId());
      vector.add(lista.get(i).getAlumno().getApellido()+" "+lista.get(i).getAlumno().getNombre());
      String MyFecha = formato.format(lista.get(i).getFecha());
      vector.add(MyFecha);
      vector.add(lista.get(i).getMonto_pago());
      contadorpag=contadorpag+lista.get(i).getMonto_pago();
      
      modeloTabla.addRow(vector);
      }
    }
     jTable2.setModel(modeloTabla); 
     jTextField15.setText(String.valueOf(contadorpag));
    }

