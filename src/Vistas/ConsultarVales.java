    private void inicializarTablaVale() {
     DefaultTableModel modeloTabla = new DefaultTableModel(); 
    Vector<String> columnas = new Vector<String>();
    modeloTabla.addColumn("Id");
    modeloTabla.addColumn("Fecha Entrega");
    modeloTabla.addColumn("Monto Entrega");
    modeloTabla.addColumn("Para");    
    jTable1.setModel(modeloTabla);           
    }

    private void buscarFechaeIntegrante() {
    SimpleDateFormat formato=new SimpleDateFormat("dd-MM-yyyy");
    List<Vale> lista=new ArrayList<>();
    lista=new FacadeVale().BuscarxFechaeIntegrante(jXDatePicker1.getDate(), jXDatePicker2.getDate(), jTextField1.getText().toUpperCase());
    DefaultTableModel modeloTabla = new DefaultTableModel(); 
    Vector<String> columnas = new Vector<String>();
    modeloTabla.addColumn("Id");
    modeloTabla.addColumn("Fecha Entrega");
    modeloTabla.addColumn("Monto Entrega");
    modeloTabla.addColumn("Para");
    modeloTabla.addColumn("Observaciones");   
       
    
    
    
    for (int i = 0; i < lista.size(); i++) {
      Vector vector = new Vector();
      vector.add(lista.get(i).getId());
      String MyFecha = formato.format(lista.get(i).getFec());
      vector.add(MyFecha);
      vector.add(lista.get(i).getMonto());
      vector.add(lista.get(i).getAnombre());
      vector.add(lista.get(i).getObservacion());
      
      
      modeloTabla.addRow(vector);
      
    }
     jTable1.setModel(modeloTabla); 
    }

