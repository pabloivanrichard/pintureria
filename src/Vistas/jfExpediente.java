    private void cargarDatosExpte(Expediente expte) {
        
        SimpleDateFormat formateador = new SimpleDateFormat("dd/MM/yyyy");
        
        txtCodigo.setText(expteVigente.getCodigo());
        txtExtracto.setText(expteVigente.getExtracto());
        txtEstado.setText(expteVigente.getEstado());
        txtOrganismo.setText(expteVigente.getOrganismo());
        
        if (expteVigente.getFechaInicio() != null){
            txtFechaInicio.setText(formateador.format(expteVigente.getFechaInicio()));
        }
        
        if (expteVigente.getFechaFin() != null){
            txtFechaFin.setText(formateador.format(expteVigente.getFechaFin()));
        }
        

        float monto = 0;

        List<Detalle_Expediente> lista;
        lista = expte.getDetalle();
        DefaultTableModel modeloTabla = new DefaultTableModel();

        Vector<String> columnas = new Vector<String>();
        modeloTabla.addColumn("Id");
        //modeloTabla.addColumn("Codigo");
        //modeloTabla.addColumn("fechaInicio");
        //modeloTabla.addColumn("fechaFin");
        modeloTabla.addColumn("Producto");
        modeloTabla.addColumn("Precio");
        modeloTabla.addColumn("Cantidad");

        for (int i = 0; i < lista.size(); i++) {
            Vector vector = new Vector();
            vector.add(lista.get(i).getId());
            vector.add(lista.get(i).getProducto().getDescripcion());
            vector.add(lista.get(i).getPrecio());
            vector.add(lista.get(i).getCantidad());
            monto = (lista.get(i).getPrecio() * lista.get(i).getCantidad()) + monto;

            modeloTabla.addRow(vector);
        }
        tblDetalleExpte.setModel(modeloTabla);
        txtMonto.setText(Float.toString(monto));
        btnEditar.setEnabled(true);
        btnQuitar.setEnabled(true);
        
    }

