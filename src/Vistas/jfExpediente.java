    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        // TODO add your handling code here:
        //se elimina el elemento
        if (expteVigente == null) {
            JOptionPane.showMessageDialog(rootPane, "No existe ningun Expte activo para eliminar");

        } else {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("pintureriaPU");
            ExpedienteJpaController expedienteJpaController = new ExpedienteJpaController(emf);
            JOptionPane.showConfirmDialog(rootPane, "Esta seguro que desea elminir el Expte? ");
            try {
                expedienteJpaController.destroy(expteVigente.getId());
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(jfExpediente.class.getName()).log(Level.SEVERE, null, ex);
            }
            limpiarComponentes();
            JOptionPane.showMessageDialog(rootPane, "La operacion se realizo con exito");
        }
    }//GEN-LAST:event_btnQuitarActionPerformed

