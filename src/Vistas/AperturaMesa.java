    private void inicializarComponentes() {
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField1.setEnabled(false);
    jTextField2.setEditable(false);
    llenartablamozo();
    Date hoy=new Date();
    jXDatePicker1.setFormats("dd/MM/yyyy");
    jXDatePicker1.setDate(hoy);
    jTextField1.setText(String.valueOf(m.getNumero()));
    }

    private void llenartablamozo() {
    List<Mozo> lista;
    lista=new FacadeMozo().listar();
    DefaultTableModel modeloTabla = new DefaultTableModel(); 
      
    Vector<String> columnas = new Vector<String>();
    modeloTabla.addColumn("Id");
    modeloTabla.addColumn("Apellido");
    modeloTabla.addColumn("Nombre");
    modeloTabla.addColumn("Dni");
    
    
    for (int i = 0; i < lista.size(); i++) {
      Vector vector = new Vector();
      vector.add(lista.get(i).getId());
      vector.add(lista.get(i).getApellido());
      vector.add(lista.get(i).getNombre());
      vector.add(lista.get(i).getDni());
      modeloTabla.addRow(vector);
    }
     jTable1.setModel(modeloTabla);

    
    }
