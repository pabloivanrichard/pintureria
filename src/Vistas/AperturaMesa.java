    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
      if(jTable1.getSelectedRow()!=-1){
      EntityManagerFactory emf = Persistence.createEntityManagerFactory("pintureriaPU");  
      String valorTabla=(jTable1.getValueAt(jTable1.getSelectedRow(),0)).toString();
      Long valor=Long.parseLong(valorTabla);
      mozo=new MozoJpaController(emf).findMozo(valor);
      jTextField2.setText(mozo.getApellido()+" "+mozo.getNombre());
      }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jTextField2.getText().equals("")){
            try {
                EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
                //Genero el servicio
                Date hoy=new Date();
                ServicioMesa servicio=new ServicioMesa();
                servicio.setFecha(hoy);
                servicio.setInicio(hoy);
                servicio.setMesa(String.valueOf(m.getNumero()));
                servicio.setMozo(jTextField2.getText().toUpperCase());
                ServicioMesaJpaController sjc=new ServicioMesaJpaController(emf);
                sjc.create(servicio);
                //Actualizo estado de Mesa
                
                MesaJpaController mjc=new MesaJpaController(emf);
                m.setCerrado(0);
                mjc.edit(m);
                jButton1.setEnabled(false);
                JOptionPane.showMessageDialog(rootPane, "La operacion se realizo con Exito");
            } catch (Exception ex) {
                Logger.getLogger(AperturaMesa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

