    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jXDatePicker1.getDate()!=null && !jTextField1.getText().equals("") && !jTextField2.getText().equals("") ){
            EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
            Vale vale=new Vale();
            vale.setFec(jXDatePicker1.getDate());
            vale.setMonto(Float.parseFloat(jTextField1.getText()));
            vale.setAnombre(jTextField2.getText().toUpperCase());
            vale.setObservacion(jTextArea1.getText());
            ValeJpaController vjc=new ValeJpaController(emf);
            vjc.create(vale);
            llenarTablaPagos();
            limpiarCampos();
            JOptionPane.showMessageDialog(rootPane, "La operacion se Realizo con exito");        
        }else {JOptionPane.showMessageDialog(rootPane, "Los Datos con * son obligatorios");}
    }//GEN-LAST:event_jButton1ActionPerformed

