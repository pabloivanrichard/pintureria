    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
      if (jTable1.getSelectedRow() != -1 && opcion==1 || opcion==2 || opcion==3) {
      EntityManagerFactory emf = Persistence.createEntityManagerFactory("pintureriaPU");  
      String valorTabla=(jTable1.getValueAt(jTable1.getSelectedRow(),0)).toString();
      Long valor=Long.parseLong(valorTabla);
      prove= new ProveedorJpaController(emf).findProveedor(valor);
     jTextField1.setText(prove.getNombre());
     jTextField2.setText(prove.getCuit());
     jTextField3.setText(prove.getTel());
     jTextField4.setText(prove.getEmail());
     jTextField5.setText(prove.getDireccion());
     jTextField6.setText(prove.getRepresentante());
     jTextField7.setText(prove.getDoc());
     jComboBox1.setSelectedItem(prove.getTipodoc().getDescripcion());
      }
    }//GEN-LAST:event_jTable1MouseClicked

