     private void cargarCombos() {
     DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
     List<Tipo_Doc> lista;
     lista=new FacadeTipoDoc().listar();
     if(lista.get(0).getId()!=null){
      for(int i=0;  i < lista.size(); i++){
      Vector vector = new Vector();
      vector.add(lista.get(i).getDescripcion());
      modeloCombo.addElement(lista.get(i).getDescripcion());
     }
     jComboBox1.setModel(modeloCombo);            
    }
    }
     public void llenarTabla(){
        List<Proveedor> lista;
        lista=new FacadeProveedor().listar();
         DefaultTableModel modeloTabla = new DefaultTableModel(); 
      
    Vector<String> columnas = new Vector<String>();
    modeloTabla.addColumn("Id");
    modeloTabla.addColumn("Proveedor");
    modeloTabla.addColumn("telefono");
    modeloTabla.addColumn("email");
    modeloTabla.addColumn("Direccion");
    modeloTabla.addColumn("Representante");
    
    for (int i = 0; i < lista.size(); i++) {
      Vector vector = new Vector();
      vector.add(lista.get(i).getId());
      vector.add(lista.get(i).getNombre());
      vector.add(lista.get(i).getTel());
      vector.add(lista.get(i).getEmail());
      vector.add(lista.get(i).getDireccion());
      vector.add(lista.get(i).getRepresentante());
      
      modeloTabla.addRow(vector);
    }
     jTable1.setModel(modeloTabla);       
     }
