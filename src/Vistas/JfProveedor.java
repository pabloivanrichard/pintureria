      prove.setNombre(jTextField1.getText().toUpperCase());
      prove.setCuit(jTextField2.getText());
      prove.setTel(jTextField3.getText());
      prove.setEmail(jTextField4.getText());
      prove.setDireccion(jTextField5.getText().toUpperCase());
      prove.setRepresentante(jTextField6.getText().toUpperCase());
      List<Tipo_Doc> td;
      td= new FacadeTipoDoc().buscarxdescripcion(jComboBox1.getSelectedItem().toString());
      prove.setTipodoc(td.get(0));
      prove.setDoc(jTextField7.getText());
