    private void CargarDatosCajaAbierta() {
    List<Caja> lista=new FacadeCaja().buscarxEstado(0);    
    if(lista.size()!=0){
    caja=lista.get(0);
    jXDatePicker1.setDate(caja.getFecha());
     SimpleDateFormat formato=new SimpleDateFormat("HH:mm:ss");
     String horainicio=formato.format(caja.getHorainicio());    
     jTextField1.setText(horainicio);
     jTextField2.setText(String.valueOf(caja.getMontoinicial()));
     jButton1.setEnabled(true);
    }else{
        JOptionPane.showMessageDialog(rootPane, "No Existen Caja Abiertas");
        jButton1.setEnabled(false);
    }
    }

    private void CargarDatosCierreCaja() {
        validarCargaDatosparacierre=1;
        EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
        Date ahora=new Date();
        jXDatePicker2.setDate(ahora);
        SimpleDateFormat formato=new SimpleDateFormat("HH:mm:ss");
        String horafin=formato.format(ahora);    
        jTextField5.setText(horafin);
        float totalingresos=0;
        float totalegresos=0;
        List<Venta> listaventa=new FacadeVenta().buscarxFechayHora(caja.getFecha(), caja.getHorainicio(),ahora);
        List<Compras> listacompras=new FacadeCompra().buscarxFechayhora(caja.getFecha(), caja.getHorainicio(), ahora);
        List<Vale> listavale=new FacadeVale().BuscarxFechayHora(caja.getHorainicio(), ahora);
        for(int i=0; i<listaventa.size();i++){
        totalingresos=totalingresos+listaventa.get(i).getMonto();
        }
        for(int j=0; j<listacompras.size();j++){
        totalegresos=totalegresos+listacompras.get(j).getPrecio_totol();
        }
        for(int z=0; z<listavale.size(); z++){
        totalegresos=totalegresos+listavale.get(z).getMonto();
        }
        float totalcaja=(caja.getMontoinicial()+totalingresos)-(totalegresos);
        jTextField3.setText(String.valueOf(totalingresos));
        jTextField4.setText(String.valueOf(totalegresos));
        jTextField6.setText(String.valueOf(totalcaja));
        caja.setCompras(listacompras);
        caja.setVentas(listaventa);
        caja.setVales(listavale);
        caja.setEstado(1);
        caja.setFechacierre(ahora);
        caja.setHorafin(ahora);
        caja.setMontoegresos(totalegresos);
        caja.setMontoingresos(totalingresos);
        caja.setMontocaja(totalcaja);
        jButton2.setEnabled(true);
    }
