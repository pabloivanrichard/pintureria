    private void ChequearCaja() {
    //Traigos las cajas si existe una caja abierta no puedo realzar operacion    
    List<Caja> listacaja=new FacadeCaja().buscarxEstado(0);
        SimpleDateFormat formato=new SimpleDateFormat("HH:mm:ss");
    if(listacaja.size()!=0){
    jXDatePicker1.setDate(listacaja.get(0).getFecha());
    String horainicio=formato.format(listacaja.get(0).getHorainicio());
    jTextField1.setText(horainicio);
    jTextField2.setText(String.valueOf(listacaja.get(0).getMontoinicial()));
    jTextField3.setText(listacaja.get(0).getUsuario().getNombre());
    //bloqueo elementos
    jTextField1.setEnabled(false);
    jTextField2.setEnabled(false);
    jTextField3.setEnabled(false);
    jXDatePicker1.setEnabled(false);
    jButton2.setEnabled(false);
    }
    if(listacaja.size()==0){
    jTextField1.setEnabled(false);
    jTextField2.setEnabled(true);
    jTextField2.setText("0");
    jTextField3.setEnabled(false);
    jXDatePicker1.setEnabled(false);
    jButton2.setEnabled(true);
    }
    
    }
