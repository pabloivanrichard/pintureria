        // TODO add your handling code here:
          if(!jTextField1.getText().equals("")&& !jTextField2.getText().equals("")){
            validarUsuario();
            if(valida==0){
                EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
                Usuario usuario=new Usuario();
                usuario.setNombre(jTextField1.getText());
                usuario.setPass(jTextField2.getText());
                usuario.setTipo(jComboBox1.getSelectedItem().toString());
                UsuarioJpaController ujc=new UsuarioJpaController(emf);
                ujc.create(usuario);
                LimpiarCampos();
                llenarTablaUsuario();
            }else{JOptionPane.showMessageDialog(rootPane, "El usuario ya Existe");}
        }else{JOptionPane.showMessageDialog(rootPane, "Debe completar los Datos del Usuario");
        }
