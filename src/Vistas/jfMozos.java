    private void cargarTabla() {
    EntityManagerFactory emf= Persistence.createEntityManagerFactory("pintureriaPU");
    List <Mozo> listap=new FacadeMozo().listar();
    DefaultTableModel modelo= new DefaultTableModel();
    modelo.addColumn("Id");
    modelo.addColumn("Apellido");
    modelo.addColumn("Nombre");
    modelo.addColumn("Dni");
    modelo.addColumn("Domicilio");
    modelo.addColumn("Telefono");
    for(int i=0; i<listap.size(); i++){
    Vector vector=new Vector();
    vector.add(listap.get(i).getId());
    vector.add(listap.get(i).getApellido());
    vector.add(listap.get(i).getNombre());
    vector.add(listap.get(i).getDni());
    vector.add(listap.get(i).getDomicilio());
    vector.add(listap.get(i).getTelefono());        
    modelo.addRow(vector);
    }
    jTable1.setModel(modelo);
   
    }
