    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().equals("")&&!jTextField2.getText().equals("")&&!jTextField3.getText().equals("")){
            EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
            Mozo mozo=new Mozo();
            mozo.setApellido(jTextField1.getText().toUpperCase());
            mozo.setNombre(jTextField2.getText().toUpperCase());
            mozo.setDni(jTextField3.getText().toUpperCase());
            if(!jTextField4.getText().equals("")){
            mozo.setDomicilio(jTextField4.getText().toUpperCase());
            }
            if(!jTextField5.getText().equals("")){
            mozo.setTelefono(jTextField5.getText());
            }
            MozoJpaController mjc=new MozoJpaController(emf);
            mjc.create(mozo);
            JOptionPane.showMessageDialog(rootPane, "La operacion se realizo con exito");
            limpiarCampos();
            cargarTabla();
        }else{JOptionPane.showMessageDialog(rootPane, "Los datos con * son Obligatorios");}
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
      if (jTable1.getSelectedRow() != -1) {
      EntityManagerFactory emf = Persistence.createEntityManagerFactory("pintureriaPU");  
      String valorTabla=(jTable1.getValueAt(jTable1.getSelectedRow(),0)).toString();
      Long valor=Long.parseLong(valorTabla);
      m= new MozoJpaController(emf).findMozo(valor);
     jTextField1.setText(m.getApellido());
     jTextField2.setText(m.getNombre());
     jTextField3.setText(m.getDni());
     jTextField4.setText(m.getDomicilio());
     jTextField5.setText(m.getTelefono());
      }
    }//GEN-LAST:event_jTable1MouseClicked

