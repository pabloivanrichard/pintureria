    private void inicializarTabla() {
        List<DetalleCuentaProvedorSinStock> pc=cs.getCuenta().getDetalle();
        SimpleDateFormat formato=new SimpleDateFormat("dd-MM-yyyy");
        EntityManagerFactory emf= Persistence.createEntityManagerFactory("pintureriaPU");
       // List<ComprasinStock> listaproducto= new FacadeComprasinStock().buscarxId(cs.getId());
        DefaultTableModel modeloTabla=new DefaultTableModel();
        modeloTabla.addColumn("Id");
        modeloTabla.addColumn("Tipo de Pago");
        modeloTabla.addColumn("Monto");
        modeloTabla.addColumn("Fecha de Pago");
        //modeloTabla.addColumn("Localizacion");
        
        
        for(int i=0; i<pc.size(); i++){
        String tp;    
        Vector vector=new Vector();
        vector.add(pc.get(i).getId());        
        //vector.add(listaproducto.get(i).get);
        if(pc.get(i).getPago() !=null){
        tp="CONTADO";    
        vector.add(tp);
        vector.add(pc.get(i).getPago().getMonto());
        String MyFecha = formato.format(pc.get(i).getPago().getFecha());
        vector.add(MyFecha);
        } 
        if(pc.get(i).getDeposito() !=null){
        tp="DEPOSITO";
        vector.add(tp);
        vector.add(pc.get(i).getDeposito().getMonto());
        String MyFecha = formato.format(pc.get(i).getDeposito().getFecha());
        vector.add(MyFecha);
       
        }
       
       // vector.add(listaproducto.get(i).getLocalizacion());
        modeloTabla.addRow(vector);
        }
        jTable1.setModel(modeloTabla);
    }
