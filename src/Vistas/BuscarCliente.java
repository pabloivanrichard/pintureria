    private void cargarCombos() {
     DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
     List<Tipo_Doc> lista;
     lista=new FacadeTipoDoc().listar();
     if(lista.get(0).getId()!=null){
      for(int i=0;  i < lista.size(); i++){
      Vector vector = new Vector();
      vector.add(lista.get(i).getDescripcion());
      modeloCombo.addElement(lista.get(i).getDescripcion());
     }
     jComboBox1.setModel(modeloCombo);            
    }
    }

    private void bucarxDocumento() {
        List<Cliente> lista;
        List<Tipo_Doc> listatd;
        listatd=new FacadeTipoDoc().buscarxdescripcion(jComboBox1.getSelectedItem().toString());
        lista=new FacadeCliente().buscarxdoc(listatd.get(0), jTextField1.getText());
        DefaultTableModel modeloTabla = new DefaultTableModel(); 
      
    Vector<String> columnas = new Vector<String>();
    modeloTabla.addColumn("Id");
    modeloTabla.addColumn("Apellido");
    modeloTabla.addColumn("Nombre");
    modeloTabla.addColumn("Tipo doc");
    modeloTabla.addColumn("Doc");
    modeloTabla.addColumn("Domicilio");
    modeloTabla.addColumn("tel");
    modeloTabla.addColumn("email");
    modeloTabla.addColumn("Tipo cliente");
    
    
    for (int i = 0; i < lista.size(); i++) {
      Vector vector = new Vector();
      vector.add(lista.get(i).getId());
      vector.add(lista.get(i).getApellido());
      vector.add(lista.get(i).getNombre());
      vector.add(lista.get(i).getTipo_doc().getDescripcion());
      vector.add(lista.get(i).getDoc());
      vector.add(lista.get(i).getDomicilio());
      vector.add(lista.get(i).getTel());
      vector.add(lista.get(i).getEmail());
      vector.add(lista.get(i).getDescripcion().getDescripcion());
     
      modeloTabla.addRow(vector);
    }
     jTable1.setModel(modeloTabla);
    }

