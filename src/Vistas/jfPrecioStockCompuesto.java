    private void cargarPorcentaje() {
    EntityManagerFactory emf= Persistence.createEntityManagerFactory("pintureriaPU");
    List <ProductoCompuesto> listap=new FacadeProductoCombinado().listar();
    int porcentaje=Integer.parseInt(jTextField31.getText());
    DefaultTableModel modelo= new DefaultTableModel();
    modelo.addColumn("Id");
   // modelo.addColumn("Codigo");
    modelo.addColumn("Producto");
    //modelo.addColumn("Proveedor");
   // modelo.addColumn("precio real");
    modelo.addColumn("precio");
    modelo.addColumn("precio aumentado");
    for(int i=0; i<listap.size(); i++){
    Vector vector=new Vector();
    vector.add(listap.get(i).getId());
    // vector.add(listap.get(i).getCod());
    vector.add(listap.get(i).getDescripcion());
    //vector.add("NEGOCIO");
    vector.add(listap.get(i).getPrecio());
    //calcular pocentaje
    float precio=listap.get(i).getPrecio();
    float preciopor=(precio*porcentaje)/100;
    float precioaumentado=precio+preciopor;
    vector.add(precioaumentado);
    modelo.addRow(vector);
    }
    jTable1.setModel(modelo);  
    }

