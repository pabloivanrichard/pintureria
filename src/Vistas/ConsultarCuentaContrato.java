    private void inicializarComponentes() {
    jTextField1.setEnabled(false);
    jTextField2.setEnabled(false);
    jTextField3.setEnabled(false);
    contrato=ConsultarContrato.contrato;
    jTextField1.setText(String.valueOf(contrato.getCuenta_contrato().getTotal()));
    jTextField2.setText(String.valueOf(contrato.getCuenta_contrato().getAnticipo()));
    float saldo=contrato.getCuenta_contrato().getTotal()-contrato.getCuenta_contrato().getAnticipo();
    jTextField3.setText(String.valueOf(saldo));
    cargarTablaPagos();
    }

    private void cargarTablaPagos() {
     
    List<detalle_cuenta_contrato> lista=new ArrayList<>();
      
    DefaultTableModel modeloTabla = new DefaultTableModel(); 
    Vector<String> columnas = new Vector<String>();
    modeloTabla.addColumn("Id");
    modeloTabla.addColumn("Nro de contrato");
    modeloTabla.addColumn("Integrante");
    modeloTabla.addColumn("Fecha Pago");
    modeloTabla.addColumn("Monto Pago");
   
    
    
    
    for (int i = 0; i < lista.size(); i++) {
      Vector vector = new Vector();
      vector.add(lista.get(i).getId());
      vector.add(contrato.getNroContrato());
      vector.add(lista.get(i).getAlumno());
      vector.add(lista.get(i).getFecha());
      vector.add(lista.get(i).getMonto_pago());
      
      
      modeloTabla.addRow(vector);
    }
     jTable1.setModel(modeloTabla); 
    
    }
