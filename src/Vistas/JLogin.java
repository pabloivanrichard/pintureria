    private void CargarLocalizacion() {
      List<Localizacion> list = new ArrayList<Localizacion>();
        FacadeLocalizacion fa=new FacadeLocalizacion();
        list=fa.ObtenerID();
        if(list==null){
        EntityManagerFactory emf=Persistence.createEntityManagerFactory("pintureriaPU");
        List<Almacen> listaalmacen=new ArrayList<Almacen>();
        listaalmacen=new FacadeAlmacen().listar();
        if(listaalmacen.size()!=0){
        for(int i=0; i< listaalmacen.size();i++){
        Localizacion localizacion=new Localizacion();
        localizacion.setCantidad(Integer.parseInt(listaalmacen.get(i).getCantidad()));
        localizacion.setDescripcion(listaalmacen.get(i).getLocalizacion());
        localizacion.setId_almacen(String.valueOf(listaalmacen.get(i).getId()));
        localizacion.setId_producto(String.valueOf(listaalmacen.get(i).getProducto().getId()));
            LocalizacionJpaController ljc=new LocalizacionJpaController(emf);
            ljc.create(localizacion);
        }
        }
        }else{
         int tamaniolista=list.size()+1;       
        }
    }
