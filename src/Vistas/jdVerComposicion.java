    private void inicializarComponentes() {
    String descripcion="";
    if(productosPrimarios.size()> 0){
    jLabel1.setText("Diagnostico: Existen componentes Principales Que no cubren la demanda, No se puede procesar");
    for(int i=0;i<productosPrimarios.size();i++){
    descripcion=descripcion+productosPrimarios.get(i);
    }
    jTextArea1.setText(descripcion);
    jButton1.setEnabled(false);
    jButton2.setEnabled(true);
    }else{
    jLabel1.setText("Diagnostico: Existen componentes Secundarios que no cubren la demanda");
    for(int i=0;i<productosSecundarios.size();i++){
    descripcion=descripcion+productosSecundarios.get(i);
    }
    jTextArea1.setText(descripcion);
    jButton1.setEnabled(true);
    jButton2.setEnabled(true);
    }
    }
